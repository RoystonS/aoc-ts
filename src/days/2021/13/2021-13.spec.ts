import { puzzle } from "./Puzzle";
import { readInput } from "../../../common";

const year = "2021";
const day = "13";

describe("2021-13", () => {
  const example = `6,10
0,14
9,10
0,3
10,4
4,11
6,0
6,12
4,1
0,13
10,12
3,4
3,0
8,4
1,10
2,14
8,10
9,0

fold along y=7
fold along x=5`;

  it("passes part1 example", () => {
    expect(puzzle.computePart1(example)).toEqual("17");
  });

  it("passes part1", () => {
    const input = readInput(year, day);
    expect(puzzle.computePart1(input)).toEqual(puzzle.part1Answer);
  });

  it("passes part2", () => {
    const input = readInput(year, day);
    expect(puzzle.computePart2(input)).toEqual(puzzle.part2Answer);
  });
});
